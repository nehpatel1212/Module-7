letter=input("Enter A to Z : ")
letter.upper()
if letter=="A":
    print(letter,"Is Vowel.")
elif letter=="E":
    print(letter,"Is Vowel.")
elif letter=="I":
    print(letter,"Is Vowel.")
elif letter=="O":
    print(letter,"Is Vowel.")
elif letter=="U":
    print(letter,"Is Vowel.")
else:
    print(letter,"Is Not Vowel.")
